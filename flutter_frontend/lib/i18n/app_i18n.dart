  static String languageLabel(String languageCode) {
    switch (languageCode) {
      case 'en-US':
        return 'English';
      case 'zh-CN':
        return '简体中文';
      case 'zh-TW':
        return '繁體中文';
      case 'he':
        return 'עברית';
      default:
        return languageCode;
    }
  }

  static String tr(String languageCode, String key) {
    final table = _messages[languageCode] ?? _messages[defaultLanguageCode]!;
    return table[key] ?? _messages[defaultLanguageCode]![key] ?? key;
  }

